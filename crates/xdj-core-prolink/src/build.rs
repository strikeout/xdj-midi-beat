use crate::{MAGIC, PKT_ANNOUNCE, PKT_CLAIM1, PKT_CLAIM2, PKT_CLAIM_FINAL, PKT_KEEPALIVE};

pub fn pad_name(name: &str) -> [u8; 20] {
    let mut buf = [0u8; 20];
    let bytes = name.as_bytes();
    let len = bytes.len().min(19);
    buf[..len].copy_from_slice(&bytes[..len]);
    buf
}

fn write_keepalive_header(p: &mut [u8], pkt_type: u8, subtype: u8, name: &[u8; 20]) {
    p[..10].copy_from_slice(&MAGIC);
    p[0x0a] = pkt_type;
    p[0x0c..0x20].copy_from_slice(name);
    p[0x20] = 0x01;
    p[0x21] = 0x04;
    p[0x23] = subtype;
}

pub fn build_announce(name: &[u8; 20]) -> [u8; 0x25] {
    let mut p = [0u8; 0x25];
    write_keepalive_header(&mut p, PKT_ANNOUNCE, 0x25, name);
    p[0x24] = 0x01;
    p
}

pub fn build_claim1(name: &[u8; 20], mac: &[u8; 6], n: u8) -> [u8; 0x2c] {
    let mut p = [0u8; 0x2c];
    write_keepalive_header(&mut p, PKT_CLAIM1, 0x2c, name);
    p[0x24] = n;
    p[0x25] = 0x01;
    p[0x26..0x2c].copy_from_slice(mac);
    p
}

pub fn build_claim2(
    name: &[u8; 20],
    ip: &[u8; 4],
    mac: &[u8; 6],
    device_num: u8,
    n: u8,
) -> [u8; 0x32] {
    let mut p = [0u8; 0x32];
    write_keepalive_header(&mut p, PKT_CLAIM2, 0x32, name);
    p[0x24..0x28].copy_from_slice(ip);
    p[0x28..0x2e].copy_from_slice(mac);
    p[0x2e] = device_num;
    p[0x2f] = n;
    p[0x30] = 0x01;
    p[0x31] = 0x01;
    p
}

pub fn build_claim_final(name: &[u8; 20], device_num: u8, n: u8) -> [u8; 0x26] {
    let mut p = [0u8; 0x26];
    write_keepalive_header(&mut p, PKT_CLAIM_FINAL, 0x26, name);
    p[0x24] = device_num;
    p[0x25] = n;
    p
}

pub fn build_keepalive(
    name: &[u8; 20],
    device_num: u8,
    mac: &[u8; 6],
    ip: &[u8; 4],
    _peers: u8,
) -> [u8; 0x36] {
    let mut p = [0u8; 0x36];
    p[..10].copy_from_slice(&MAGIC);
    p[0x0a] = PKT_KEEPALIVE;
    let name_len = name.len().min(19);
    p[0x0c..0x0c + name_len].copy_from_slice(&name[..name_len]);
    p[0x20] = 0x01;
    p[0x21] = 0x04;
    p[0x23] = 0x36;
    p[0x24] = device_num;
    p[0x25] = 0x04;
    p[0x26..0x2c].copy_from_slice(mac);
    p[0x2c..0x30].copy_from_slice(ip);
    p[0x30] = 0x01;
    p[0x34] = 0x04;
    p[0x35] = 0x00;
    p
}

